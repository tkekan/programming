def findMaxConsecutiveOnes(nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        low = 0
        high = 0
        maxlen = 0
        presum = 0
        for index in range(0,len(nums)):
            if nums[index] == 0:
                presum += 1
            if presum <= 1:
                high += 1
                maxlen = max(maxlen,high-low)
            if presum > 1:
                while nums[low] == 1 and low < high:
                    low += 1
                maxlen = max(maxlen,high - low)
        return maxlen

